BgfxTestView::BgfxTestView()
{
	PosColorVertex::init();

	m_vbh = bgfx::createVertexBuffer(
		bgfx::makeRef(s_cubeVertices, sizeof(s_cubeVertices)), PosColorVertex::ms_layout);

	m_ibh = bgfx::createIndexBuffer(
		bgfx::makeRef(s_cubeTriList, sizeof(s_cubeTriList)));

	const bgfx::Memory* vmem = bgfx::alloc(sizeof(vs_cubes_mtl));
	bx::memCopy(vmem->data, vs_cubes_mtl, sizeof(vs_cubes_mtl));
	bgfx::ShaderHandle vh = bgfx::createShader(vmem);

	const bgfx::Memory* fmem = bgfx::alloc(sizeof(fs_cubes_mtl));
	bx::memCopy(fmem->data, fs_cubes_mtl, sizeof(fs_cubes_mtl));
	bgfx::ShaderHandle fh = bgfx::createShader(fmem);

	m_ph = bgfx::createProgram(vh, fh);

	m_timeOffset = bx::getHPCounter();
}

void BgfxTestView::Update(int width, int height)
{
	bgfx::setViewRect(2, 0, 0, uint16_t(width), uint16_t(height));

	float time = (float)((bx::getHPCounter() - m_timeOffset) / double(bx::getHPFrequency()));

	const bx::Vec3 at = {0.0f, 0.0f, 0.0f};
	const bx::Vec3 eye = {0.0f, 0.0f, -35.0f};

	float view[16];
	bx::mtxLookAt(view, eye, at);

	float proj[16];
	bx::mtxProj(proj, 60.0f, float(width) / float(height), 0.1f, 100.0f, bgfx::getCaps()->homogeneousDepth);
	bgfx::setViewTransform(2, view, proj);

	uint64_t state = 0 | BGFX_STATE_WRITE_R | BGFX_STATE_WRITE_G | BGFX_STATE_WRITE_B | BGFX_STATE_WRITE_A | BGFX_STATE_WRITE_Z | BGFX_STATE_DEPTH_TEST_LESS | BGFX_STATE_CULL_CW | BGFX_STATE_MSAA;

	for (uint32_t yy = 0; yy < 2; ++yy)
	{
		for (uint32_t xx = 0; xx < 2; ++xx)
		{
			float mtx[16];
			bx::mtxRotateXY(mtx, time + xx * 0.21f, time + yy * 0.37f);
			mtx[12] = -15.0f + float(xx) * 3.0f;
			mtx[13] = -15.0f + float(yy) * 3.0f;
			mtx[14] = 0.0f;

			// Set model matrix for rendering.
			bgfx::setTransform(mtx);

			// Set vertex and index buffer.
			bgfx::setVertexBuffer(0, m_vbh);
			bgfx::setIndexBuffer(m_ibh);

			// Set render states.
			bgfx::setState(state);

			// Submit primitive for rendering to view 0.
			bgfx::submit(2, m_ph);
		}
	}
}